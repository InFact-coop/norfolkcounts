    <h1>How do we know Leeds' impact?</h1>

    <p>In order to determine Leeds’ contribution to the UK Aid budget, we used Leeds’ regional Gross Value Added (GVA) as a percentage of the UK’s total GVA as a proxy for Leeds’ estimated contribution to the economy. Given the UK government does not produce regional GNI, we have elected to use UK GVA as an equivalent measure to GNI.</p>

    <p>The total calculation of Leeds’ contribution to UK Aid is as follows:</p>

    <ul>
      <li>• Leeds total GVA is £21,951 million</li>
      <li>• UK GVA less Extra-Regio is £1,729,082 million</li>
      <li>• Leeds total GVA/ UK GVA less Extra-Regio = Leeds’ percentage contributed to ODA or 1.2659%</li>
    </ul>

    <p>With an overall UK Aid budget of £13,933 million, Leeds’ portion of said budget is £176,378,000 or 1.2659% of overall ODA. Applying the figure of 1.2659% to general UK Aid statistics, we can determine numbers like the following:</p>

    <ul>
      <li>• The UK delivered 1950 tonnes of emergency Relief</li>
      <li>• 1.2659% of 1950 tonnes of emergency relief = 24.68 tonnes</li>
      <li>• Leeds delivered 24.68 tonnes of emergency relief</li>
    </ul>

    <p>We’re grateful for the support of the Office of National Statistics and volunteer scientists at DataKind who helped make our thinking robust.</p>

