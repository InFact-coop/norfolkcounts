    <h1>How do we know Norfolk's impact?</h1>

    <p>
      In order to determine Norfolk’s contribution to the UK Aid budget, we used
      Norfolk’s regional Gross Value Added (GVA) as a percentage of the UK’s
      total GVA as a proxy for Norfolk’s estimated contribution to the economy.
      Given the UK government does not produce regional GNI, we have elected to
      use UK GVA as an equivalent measure to GNI.
    </p>

    <p>
      The total calculation of Norfolk's contribution to UK Aid is as follows:
    </p>

    <ul>
      <li class="i b">
        • Norfolk (Norwich and East Norfolk, North and West Norfolk, Breckland
        and South Norfolk) total GVA is £19,089 million
      </li>
      <li class="i b">• UK GVA less Extra-Regio is £1,729,082 million</li>
      <li class="i b">
        • Norfolk total GVA/ UK GVA less Extra-Regio = Norfolk’s percentage
        contributed to ODA or 1.05%
      </li>
    </ul>

    <p>
      With an overall UK Aid budget of £13,933 million, Norfolk’s portion of
      said budget is 1.05% of the total amount. Applying the figure of 1.05% to
      general UK Aid statistics, we can determine numbers the following:
    </p>

    <ul>
      <li class="i b">• The UK delivered 1950 tonnes of emergency Relief</li>
      <li class="i b">
        • 1.05% of 1950 tonnes of emergency relief = 20.5 tonnes
      </li>
      <li class="i b">• Norfolk delivered 20.5 tonnes of emergency relief</li>
    </ul>

    <p>
      We’re grateful for the support of the Office of National Statistics and
      volunteer scientists at DataKind who helped make our calculations robust.
    </p>
